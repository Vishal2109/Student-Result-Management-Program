    private void addNewStudent_saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addNewStudent_saveButtonActionPerformed
        // TODO add your handling code here:
        String course = addNewStudent_course.getSelectedItem().toString();
        String branch = addNewStudent_branch.getSelectedItem().toString();
        String rollNo = addNewStudent_rollNo.getText();
        String studentName = addNewStudent_name.getText();
        String fatherName = addNewStudent_fatherName.getText();
        String gender = addNewStudent_gender.getSelectedItem().toString();
        try{
//            checks if entered roll no is valid or not
            if(rollNo.length()==0){
                JOptionPane.showMessageDialog(null, "Roll number cannot be empty.", "Warning!", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if(rollNo.length()<=2){
                JOptionPane.showMessageDialog(null, "Invalid roll number for selected branch.", "Warning!", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if(branch.compareTo("Mechanical")==0){
                if(!rollNo.startsWith("ME") || rollNo.charAt(2)<'0' || rollNo.charAt(2)>'9'){
                    JOptionPane.showMessageDialog(null, "Invalid roll number for selected branch.", "Warning!", JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }
            if(branch.compareTo("Civil")==0){
                if(!rollNo.startsWith("CE") || rollNo.charAt(2)<'0' || rollNo.charAt(2)>'9'){
                    JOptionPane.showMessageDialog(null, "Invalid roll number for selected branch.", "Warning!", JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }
            if(branch.compareTo("Electronics")==0){
                if(!rollNo.startsWith("EE") || rollNo.charAt(2)<'0' || rollNo.charAt(2)>'9'){
                    JOptionPane.showMessageDialog(null, "Invalid roll number for selected branch.", "Warning!", JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }
            if(branch.compareTo("Computer Science")==0){
                if(rollNo.length()<=3 || !rollNo.startsWith("CSE") || rollNo.charAt(3)<'0' || rollNo.charAt(3)>'9'){
                    JOptionPane.showMessageDialog(null, "Invalid roll number for selected branch.", "Warning!", JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }
            
//            connects to Databse
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "Qwerty@2109");
            
            Statement st = con.createStatement();
//            updates information on students
            st.executeUpdate("insert into students(rollno,course,branch,name,gender,fathername) "
                    + "values('"+rollNo+"','"+course+"','"+branch+"','"+studentName+"','"+gender+"','"+fatherName+"')");
            JOptionPane.showMessageDialog(null, "Data Saved Successfully.", "", JOptionPane.INFORMATION_MESSAGE);
            
            setVisible(false);
            new addNewStudent().setVisible(true);
  
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString(), "Error!", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_addNewStudent_saveButtonActionPerformed

