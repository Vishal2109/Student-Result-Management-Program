    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        studentResultRollNo.setText(rollNo);
//        String name;
//        String course;
//        String 
        try{
//            connects with database
             Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "Qwerty@2109");
            
            Statement st = con.createStatement();
//            get information from students and result for given roll no
            ResultSet rs = st.executeQuery("SELECT * FROM students INNER JOIN result WHERE students.rollno = '"+rollNo+"'"
                    + "AND result.rollno='"+rollNo+"'");
//            fill data in relevent text fields
            if(rs.next()){
                studentResultCourse.setText(rs.getString(2));
                studentResultBranch.setText(rs.getString(3));
                studentResultName.setText(rs.getString(4));
                studentResultGender.setText(rs.getString(5));
                studentResultFatherName.setText(rs.getString(6));
                
                studentResultPhysics.setText(rs.getString(8));
                studentResultMaths.setText(rs.getString(9));
                studentResultEM.setText(rs.getString(10));
                studentResultDBMS.setText(rs.getString(11));
                studentResultOS.setText(rs.getString(12));
                
//                gets total marks
                int totalMarks = Integer.parseInt(studentResultPhysics.getText());
                totalMarks += Integer.parseInt(studentResultMaths.getText());
                totalMarks += Integer.parseInt(studentResultEM.getText());
                totalMarks += Integer.parseInt(studentResultDBMS.getText());
                totalMarks += Integer.parseInt(studentResultOS.getText());
                
                studentResultTotalMarks.setText(String.valueOf(totalMarks));
//                pass fail condition
                if(Integer.parseInt(studentResultPhysics.getText())>30 &&
                        Integer.parseInt(studentResultMaths.getText())>30 &&
                        Integer.parseInt(studentResultEM.getText())>30 &&
                        Integer.parseInt(studentResultDBMS.getText())>30 &&
                        Integer.parseInt(studentResultOS.getText())>30){
                    passFail.setText("PASS");
                    passFail.setBackground(Color.green);
                }
                else{
                    passFail.setText("FAIL");
                    passFail.setBackground(Color.red);
                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString(), "Error!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_formComponentShown

