    private void studentResultPrintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_studentResultPrintActionPerformed
        // TODO add your handling code here:
        String path = "";
        JFileChooser j = new JFileChooser();
        j.setFileSelectionMode(JFileChooser.SAVE_DIALOG);
        int x = j.showSaveDialog(this);
        if(x == JFileChooser.APPROVE_OPTION){
            path = j.getSelectedFile().getPath();
        }
        else{
            JOptionPane.showMessageDialog(null, "Something went wrong!", "Error!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        Document doc = new Document();
        try{
            PdfWriter.getInstance(doc, new FileOutputStream(path + "\\"+studentResultRollNo.getText()+"Result.pdf"));
            
            doc.open();
            
            doc.add(new Paragraph("Roll Number : " + studentResultRollNo.getText()));
            doc.add(new Paragraph("Name : " + studentResultName.getText()));
            doc.add(new Paragraph("Course : " + studentResultCourse.getText()));
            doc.add(new Paragraph("Branch : " + studentResultBranch.getText()));
            doc.add(new Paragraph("Father's Name : " + studentResultFatherName.getText()));
            doc.add(new Paragraph("Gender : " + studentResultGender.getText()));
            doc.add(new Paragraph("\n"));
            PdfPTable table = new PdfPTable(4); 
            table.addCell("Subject");
            table.addCell("Total Marks");
            table.addCell("Passing Marks");
            table.addCell("Marks Obtained");
            table.addCell("Physics");
            table.addCell("100");
            table.addCell("30");
            table.addCell(studentResultPhysics.getText());
            table.addCell("Maths");
            table.addCell("100");
            table.addCell("30");
            table.addCell(studentResultMaths.getText());
            table.addCell("EM");
            table.addCell("100");
            table.addCell("30");
            table.addCell(studentResultEM.getText());
            table.addCell("DBMS");
            table.addCell("100");
            table.addCell("30");
            table.addCell(studentResultDBMS.getText());
            table.addCell("OS");
            table.addCell("100");
            table.addCell("30");
            table.addCell(studentResultOS.getText());
            
            doc.add(table);
            
            doc.add(new Paragraph(passFail.getText()));
            
            JOptionPane.showMessageDialog(null, "Pdf saved. ","", JOptionPane.INFORMATION_MESSAGE);
            doc.close();
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString(), "Error!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_studentResultPrintActionPerformed

