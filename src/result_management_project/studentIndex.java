    private void seeResultButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seeResultButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = studentIndex_rollNo.getText();
        if(rollNo.length()==0){
            JOptionPane.showMessageDialog(null, "Roll number cannot be empty!", "Warning!", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try{
//            connects to database
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "Qwerty@2109");
            
            Statement st = con.createStatement();
            
//            checks if given roll no exist in students table
            ResultSet rs = st.executeQuery("SELECT * FROM students WHERE rollno = '"+rollNo+"'");
            if(rs.next()){
//              checks if given roll no exist in result table
                ResultSet rs1 = st.executeQuery("SELECT * FROM result WHERE rollno = '"+rollNo+"'");
                if(!rs1.next()){
                    JOptionPane.showMessageDialog(null, "Result not updated yet.\n "
                        + "Please contact admin department.", "Error!", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                setVisible(false);
                new studentResult(rollNo).setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null, "No such roll number is registered.\n "
                        + "Please contact admin department.", "Error!", JOptionPane.ERROR_MESSAGE);
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString(), "Error!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_seeResultButtonActionPerformed

