    private void addResult_SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addResult_SaveButtonActionPerformed
        // TODO add your handling code here:
//        get eneterd values
        String rollNo = addResult_rollNo.getText();
        String physics = addResult_physics.getText();
        String maths = addResult_maths.getText();
        String em = addResult_em.getText();
        String dbms = addResult_dbms.getText();
        String os = addResult_os.getText();
        
        try{
//            connects to database
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "Qwerty@2109");
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM students WHERE rollno = '"+rollNo+"'");
//            checks if roll number is registered or not
            if(rs.next()){
                st.executeUpdate("INSERT INTO RESULT (rollno,physics,maths,em,dbms,os) VALUES ('"+rollNo+"','"+physics+"','"+maths+"',"
                        + "'"+em+"','"+dbms+"','"+os+"')");
                
                JOptionPane.showMessageDialog(null, "Result saved successfully.", "", JOptionPane.INFORMATION_MESSAGE);
                
                setVisible(false);
                new addResult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null, "No such roll number is registered.", "Error!", JOptionPane.ERROR_MESSAGE);
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString(), "Error!", JOptionPane.ERROR_MESSAGE);
        }
        

    }//GEN-LAST:event_addResult_SaveButtonActionPerformed

