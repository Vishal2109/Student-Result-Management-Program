/**
 * Event handler for the login button in the admin index content panel.
 * Validates the admin credentials and navigates to the "Add New Student" panel if credentials are correct.
 */
    private void contentPanel_adminIndex_loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_contentPanel_adminIndex_loginButtonActionPerformed
        // Get the entered username and password from the admin index content panel
        String username = contentPanel_adminIndex_username.getText();
        String password = contentPanel_adminIndex_password.getText();
        
// Check if the entered credentials are valid
        if(database.checkCredentials(username, password)){
            navigationPanel.removeAll();
            navigationPanel.add(navigationPanel_addNewStudent);
            navigationPanel.repaint();
            navigationPanel.revalidate();
        
            contentPanel.removeAll();
            contentPanel.add(contentPanel_addNewStudent);
            contentPanel.repaint();
            contentPanel.revalidate();
            
            // After login is successfull  set username and password to their default values in Admin Index content 
            contentPanel_adminIndex_username.setText("username");
            contentPanel_adminIndex_password.setText("password");
        }
    }//GEN-LAST:event_contentPanel_adminIndex_loginButtonActionPerformed

