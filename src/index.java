/**
 * Event handler for the save button in the "Add New Student" content panel.
 * Validates the entered roll number and branch, adds a new student to the database,
 * and resets input fields on successful save.
 */
    private void contentPanel_addNewStudent_saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_contentPanel_addNewStudent_saveButtonActionPerformed
        // Get the entered roll number and selected branch from input fields
        String rollNo = contentPanel_addNewStudent_rollNo.getText();
        String branch = contentPanel_addNewStudent_branch.getSelectedItem().toString();
        // Validate the roll number and branch using the rollNoValidator
        if(validator.checkRollNo(rollNo, branch)){
            // If validation succeeds, add a new student to the database
            database.addNewStudent();
            contentPanel_addNewStudent_rollNo.setText("");
            contentPanel_addNewStudent_name.setText("");
            contentPanel_addNewStudent_fatherName.setText("");
            contentPanel_addNewStudent_course.setSelectedIndex(0);
            contentPanel_addNewStudent_branch.setSelectedIndex(0);
            contentPanel_addNewStudent_gender.setSelectedIndex(0);
        }
    }//GEN-LAST:event_contentPanel_addNewStudent_saveButtonActionPerformed

