/**
 * Event handler for the "See Result" button in the student index content panel.
 * Retrieves and displays the result for the specified roll number, navigating to the result panel.
 */
    private void contentPanel_studentIndex_seeResultButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_contentPanel_studentIndex_seeResultButtonActionPerformed
        // Get the roll number entered in the student index content panel
        String rollNo = contentPanel_studentIndex_rollNo.getText();
        
        // Check if the student with the entered roll number is registered
        if(database.isStudentRegistered(rollNo)){
            // check if the result for entered roll no is upated
            if(database.isResultUpdated(rollNo)){
                // Show the result for the specified roll number
            database.showStudentResult(rollNo);
            
            navigationPanel.removeAll();
            navigationPanel.add(navigationPanel_studentResult);
            navigationPanel.repaint();
            navigationPanel.revalidate();
        
            contentPanel.removeAll();
            contentPanel.add(contentPanel_studentResult);
            contentPanel.repaint();
            contentPanel.revalidate();
            }
            else{
                JOptionPane.showMessageDialog(null, "Result not updated yet. \n"
                        + "Please contact admin department.", "Error!", JOptionPane.ERROR_MESSAGE);
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "No such roll number is registered. \n"
                        + "Please contact admin department.", "Error!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_contentPanel_studentIndex_seeResultButtonActionPerformed

